from scipy.optimize import linprog
import numpy as np

# целевая функция: максимизируем 8x1 + 5x2 + 10x3
# linprog минимизирует, поэтому берём отрицательные коэффициенты
c = [-8, -5, -10]

# ограничения типа "меньше или равно"
A_ub = [[1, 2, 3], 
        [2, 1, 3]]
b_ub = [180, 200]

#огр типа "равно" (после приведения к канонической форме)
# Для x3 >= 20: -x3 <= -20
# Для x1 + x2 >= 30: -x1 - x2 <= -30

A_ub.extend([[-0, -0, -1],  # -x3 <= -20
             [-1, -1, 0]])   # -x1 - x2 <= -30
b_ub.extend([-20, -30])

# Неотрицательность переменных
bounds = [(0, None), (0, None), (0, None)]

res = linprog(c, A_ub=A_ub, b_ub=b_ub, bounds=bounds, method='highs')
print(f"Оптимальное значение F = {-res.fun}")
print(f"Оптимальное решение: x1 = {res.x[0]:.1f}, x2 = {res.x[1]:.1f}, x3 = {res.x[2]:.1f}")